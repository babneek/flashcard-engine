def get_prompt_for_subject(subject: str) -> str:
    """Get the appropriate prompt template for a subject."""
    return SUBJECT_PROMPTS.get(subject.lower(), SUBJECT_PROMPTS["general"])


def sort_cards_by_difficulty(cards: list[dict]) -> list[dict]:
    """
    Sort cards by difficulty level: beginner → intermediate → advanced.
    Also sorts by type within each difficulty level.
    """
    # Define difficulty order
    difficulty_order = {"beginner": 0, "intermediate": 1, "advanced": 2}
    
    # Define type order for each subject
    type_order = {
        # History
        "definition": 0, "timeline": 1, "people": 2, "cause-effect": 3, "analysis": 4,
        # Math
        "formula": 0, "concept": 2, "example": 3, "practice": 4,
        # Science
        "fact": 1, "process": 2, "experiment": 4,
        # General
        "default": 5
    }
    
    def get_sort_key(card):
        difficulty = card.get("difficulty", "intermediate")
        card_type = card.get("type", "concept")
        
        diff_value = difficulty_order.get(difficulty, 1)
        type_value = type_order.get(card_type, 5)
        
        return (diff_value, type_value)
    
    return sorted(cards, key=get_sort_key)


def generate_cards_from_topics(topics: list[dict], subject: str = "general") -> list[dict]:
    """
    Generate flashcards from organized topics.
    Prioritizes high-importance topics and generates more cards for them.
    
    Args:
        topics: List of dicts with 'topic', 'content', and 'importance' keys
        subject: Subject type (history, mathematics, science, general)
    
    Returns:
        List of flashcard dicts
    """
    all_cards = []
    
    # Sort topics by importance (high -> medium -> low)
    importance_order = {"high": 0, "medium": 1, "low": 2}
    sorted_topics = sorted(topics, key=lambda t: importance_order.get(t.get("importance", "medium"), 1))
    
    for topic_data in sorted_topics:
        topic_name = topic_data.get("topic", "Unknown Topic")
        content = topic_data.get("content", "")
        importance = topic_data.get("importance", "medium")
        
        if not content or len(content.split()) < 20:
            continue
        
        # Generate cards for this topic
        cards = generate_cards_from_text(content, topic_context=topic_name, subject=subject)
        
        # Adjust number of cards based on importance
        if importance == "high":
            max_cards = 15
        elif importance == "medium":
            max_cards = 10
        else:
            max_cards = 6
        
        # Add topic tag to each card
        for card in cards[:max_cards]:
            card["topic"] = topic_name
            all_cards.append(card)
    
    # Sort all cards by difficulty (beginner → intermediate → advanced)
    all_cards = sort_cards_by_difficulty(all_cards)
    
    return all_cards


def generate_cards_from_text(text: str, topic_context: str = None, subject: str = "general") -> list[dict]:
    """
    Generate flashcards from text using Groq API.
    Falls back to rule-based generation only if API key is missing.
    """
    if not GROQ_API_KEY:
        print("⚠ No GROQ_API_KEY found - using rule-based fallback")
        return _generate_rule_based(text, topic_context, subject)

    try:
        cards = _generate_with_groq(text, topic_context, subject)
        if cards:
            return cards
        print("⚠ Groq returned 0 cards - using rule-based fallback")
        return _generate_rule_based(text, topic_context, subject)
    except Exception as e:
        print(f"❌ Groq generation FAILED: {type(e).__name__}: {e}")
        import traceback
        traceback.print_exc()
        print("⚠ Falling back to rule-based generation")
        return _generate_rule_based(text, topic_context, subject)


def _generate_with_groq(text: str, topic_context: str = None, subject: str = "general") -> list[dict]:
    """Generate cards using Groq API."""
    from groq import Groq

    print(f"  🤖 Calling Groq API (key: {GROQ_API_KEY[:8]}...) model: llama-3.3-70b-versatile")
    client = Groq(api_key=GROQ_API_KEY)
    
    prompt_template = get_prompt_for_subject(subject)
    text_with_context = f"TOPIC: {topic_context}\n\n{text}" if topic_context else text
    prompt = prompt_template.format(text_excerpt=text_with_context[:3500])

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": f"You are an expert {subject} educator. Generate high-quality flashcards. Return ONLY valid JSON array, no other text."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.3,
        max_tokens=3000,
    )

    content = response.choices[0].message.content.strip()
    print(f"  ✅ Groq responded ({len(content)} chars)")
    cards = _parse_cards_json(content)
    print(f"  ✅ Parsed {len(cards)} valid cards from Groq")
    return cards


def _generate_rule_based(text: str, topic_context: str = None, subject: str = "general") -> list[dict]:
    """
    Improved rule-based card generation when no API key is available.
    Extracts key concepts and creates meaningful Q&A pairs.
    
    Args:
        text: Text content
        topic_context: Optional topic name for better card generation
        subject: Subject type for appropriate card generation
    """
    # Split into sentences
    sentences = [s.strip() for s in re.split(r'[.!?]+', text) if len(s.strip()) > 30]
    
    if not sentences:
        return []
    
    cards = []
    
    # Extract key terms (capitalized words, numbers, formulas)
    key_terms = set()
    for sentence in sentences:
        # Find capitalized terms (likely important concepts)
        caps = re.findall(r'\b[A-Z][a-z]+(?:\s+[A-Z][a-z]+)*\b', sentence)
        key_terms.update(caps)
        
        # Find numbers with context (dates, measurements)
        numbers = re.findall(r'\b\d+(?:\.\d+)?\s*(?:years?|days?|meters?|km|kg|%|°C)?\b', sentence)
        key_terms.update(numbers)
    
    # Generate definition cards for key terms
    for term in list(key_terms)[:5]:
        # Find sentence containing this term
        for sentence in sentences:
            if term in sentence:
                cards.append({
                    "front": f"What is {term}?",
                    "back": sentence,
                    "type": "definition",
                    "difficulty": "beginner"
                })
                break
    
    # Generate concept cards from important sentences
    important_sentences = [s for s in sentences if len(s.split()) > 15 and len(s.split()) < 50]
    
    for i, sentence in enumerate(important_sentences[:8]):
        # Create different question types based on sentence structure
        if "because" in sentence.lower() or "due to" in sentence.lower():
            # Cause-effect card
            parts = re.split(r'\bbecause\b|\bdue to\b', sentence, flags=re.IGNORECASE)
            if len(parts) == 2:
                cards.append({
                    "front": f"Why {parts[0].strip().lower()}?",
                    "back": f"Because {parts[1].strip()}",
                    "type": "cause-effect" if subject == "history" else "concept",
                    "difficulty": "intermediate"
                })
        elif any(word in sentence.lower() for word in ["formula", "equation", "="]):
            # Formula card
            cards.append({
                "front": f"What is the formula mentioned in: {sentence[:50]}...?",
                "back": sentence,
                "type": "formula",
                "difficulty": "beginner"
            })
        elif re.search(r'\b(is|are|was|were)\b', sentence):
            # Definition/fact card
            match = re.search(r'(.+?)\s+(is|are|was|were)\s+(.+)', sentence)
            if match:
                subject_part = match.group(1).strip()
                predicate = match.group(3).strip()
                cards.append({
                    "front": f"What {match.group(2)} {subject_part}?",
                    "back": predicate,
                    "type": "fact" if subject == "science" else "definition",
                    "difficulty": "beginner"
                })
        else:
            # General concept card
            # Extract first few words as topic
            words = sentence.split()
            topic_words = " ".join(words[:5])
            cards.append({
                "front": f"Explain: {topic_words}...",
                "back": sentence,
                "type": "concept",
                "difficulty": "intermediate"
            })
    
    # Add a summary card
    if len(sentences) >= 3:
        summary = ". ".join(sentences[:3])
        cards.append({
            "front": f"Summarize the main points{f' about {topic_context}' if topic_context else ''}.",
            "back": summary,
            "type": "analysis",
            "difficulty": "advanced"
        })
    
    # Remove duplicates and limit
    seen = set()
    unique_cards = []
    for card in cards:
        key = (card["front"], card["back"])
        if key not in seen:
            seen.add(key)
            unique_cards.append(card)
    
    return unique_cards[:12]


def _parse_cards_json(content: str) -> list[dict]:
    """Parse and validate JSON card output from LLM with strict quality control."""
    content = re.sub(r"^```(?:json)?\s*", "", content, flags=re.MULTILINE)
    content = re.sub(r"\s*```$", "", content, flags=re.MULTILINE)
    content = content.strip()

    try:
        cards = json.loads(content)
    except json.JSONDecodeError:
        match = re.search(r'\[.*\]', content, re.DOTALL)
        if match:
            cards = json.loads(match.group())
        else:
            raise ValueError("Could not parse card JSON from LLM response")

    valid_cards = []
    rejected_count = 0
    
    for card in cards:
        front = card.get("front", "").strip()
        back = card.get("back", "").strip()
        card_type = card.get("type", "concept")
        difficulty = card.get("difficulty", "intermediate")

        # Quality checks
        if len(front) < 10 or len(back) < 15:
            rejected_count += 1
            continue
            
        # Reject incomplete or low-quality cards
        if any(pattern in front.lower() for pattern in [
            "define:", "what is the formula for we", "explain the concept of we",
            "give an example/solved problem for:", "solve this challenge related to"
        ]):
            # Check if it's actually a proper question
            if not front.endswith("?") and "what" not in front.lower() and "how" not in front.lower():
                rejected_count += 1
                continue
        
        # Reject cards with incomplete sentences
        if front.count(" ") < 3 or back.count(" ") < 5:
            rejected_count += 1
            continue
            
        # Reject cards that are just fragments
        if not any(char in front for char in "?!.") and len(front.split()) < 5:
            rejected_count += 1
            continue
        
        # Truncate if too long
        if len(front) > 500:
            front = front[:500]
        if len(back) > 800:
            back = back[:800]

        # Validate card type
        valid_types = ["definition", "concept", "example", "formula", "practice", "timeline", 
                      "people", "cause-effect", "analysis", "fact", "process", "experiment"]
        if card_type not in valid_types:
            card_type = "concept"
        
        # Validate difficulty
        if difficulty not in ["beginner", "intermediate", "advanced"]:
            difficulty = "intermediate"

        valid_cards.append({
            "front": front, 
            "back": back, 
            "type": card_type,
            "difficulty": difficulty
        })
    
    if rejected_count > 0:
        print(f"  ⚠ Rejected {rejected_count} low-quality cards")

    return valid_cards
