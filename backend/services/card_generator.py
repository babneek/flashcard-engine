def generate_cards_with_pageindex(json_path: str, subject: str = "general") -> list[dict]:
    """
    Generate flashcards using the structure generated by the LOCAL PageIndex clone.
    Traverses the JSON tree to generate cards for each significant node.
    """
    import json
    with open(json_path, "r", encoding="utf-8") as f:
        structure = json.load(f)

    # Helper function to get text from all nodes in the tree
    def extract_node_texts(nodes, result_list):
        if isinstance(nodes, list):
            for node in nodes:
                extract_node_texts(node, result_list)
        elif isinstance(nodes, dict):
            # Only use nodes with substantial text
            text = nodes.get("text", "")
            if len(text.split()) > 100:
                result_list.append(text)
            
            if "nodes" in nodes:
                extract_node_texts(nodes["nodes"], result_list)

    all_texts = []
    extract_node_texts(structure, all_texts)

    # If the tree is empty or too small, fall back to extracting text from the file (handled in route)
    if not all_texts:
        return []

    # Generate cards for each extracted text block
    all_cards = []
    # Limit to top 10 sections to avoid huge costs/time unless it's a massive doc
    for text in all_texts[:10]:
        cards = generate_cards_from_text(text, subject=subject)
        all_cards.extend(cards)

    return all_cards

